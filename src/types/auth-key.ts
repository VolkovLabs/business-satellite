/**
 * Authentication Key
 */
export interface AuthKey {
  /**
   * Id
   *
   * @type {number}
   */
  id: number;

  /**
   * Name
   *
   * @type {string}
   */
  name: string;

  /**
   * Role
   *
   * @type {string}
   */
  role: string;

  /**
   * Expiration
   *
   * @type {string}
   */
  expiration: string;
}
